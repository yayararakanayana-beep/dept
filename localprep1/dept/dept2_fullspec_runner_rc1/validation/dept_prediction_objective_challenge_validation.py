"""Objective challenge validation for the DEPT prediction module.

This validation builds a balanced, public-trace-only challenge set with multiple
future dynamics directions, noise levels, horizons, and seeds.

It intentionally does not define performance pass/fail thresholds. The goal is
measurement: direction match rate, mean error, max error, p95 error, seed
stability, baseline deltas, and worst observed cases.

Boundary:
  - The prediction module receives only current public trace, public-history
    projection, public O-like context, and public G/K placeholders.
  - The true future label is generated by the validation harness and used only
    after prediction for scoring.
  - No v2 hidden/game/resource/information/action-effect traces are used.
"""
from __future__ import annotations

from dataclasses import dataclass
from typing import Dict
import random

import pandas as pd

from dept2_fullspec_runner_rc1.modules.dept_prediction_module import DEPTPredictionModule
from dept2_fullspec_runner_rc1.validation.dept_prediction_v2_validation_bench import (
    FORBIDDEN_PREDICTION_INPUT_KEYS,
    build_placeholder_gk,
    build_public_ot_tables,
    project_public_trace_from_history,
)

PREDICTION_METHOD = "prediction_module"
NEUTRAL_BASELINE_METHOD = "neutral_zero_baseline"
PREVIOUS_STEP_BASELINE_METHOD = "previous_step_baseline"
OBJECTIVE_PATTERNS = ["neutral", "overconvergence", "fixation", "divergence"]
HORIZONS = [1, 2, 3, 5]
NOISE_LEVELS = [0.0, 0.02, 0.05]
ENTITY_IDS = [f"E{i}" for i in range(8)]
RELATION_PAIRS = [
    ("E0", "E1"),
    ("E1", "E2"),
    ("E2", "E3"),
    ("E3", "E4"),
    ("E4", "E5"),
    ("E5", "E6"),
    ("E6", "E7"),
    ("E0", "E7"),
]
ALLOWED_DIRECTIONS = {"neutral", "overconvergence", "fixation", "divergence"}


@dataclass(frozen=True)
class PredictionObjectiveChallengeConfig:
    seeds: tuple[int, ...] = (101, 202, 303, 404, 505)
    patterns: tuple[str, ...] = tuple(OBJECTIVE_PATTERNS)
    noise_levels: tuple[float, ...] = tuple(NOISE_LEVELS)
    horizons: tuple[int, ...] = tuple(HORIZONS)
    history_steps: int = 6
    source_steps: int = 5


def _clip01(value: float) -> float:
    return max(0.0, min(1.0, float(value)))


def _p95(series: pd.Series) -> float:
    return float(pd.to_numeric(series, errors="coerce").fillna(0.0).quantile(0.95))


def _noise(seed: int, pattern_index: int, t: int, item_index: int, field_index: int, noise_level: float) -> float:
    if noise_level <= 0.0:
        return 0.0
    rng = random.Random(seed * 1000003 + pattern_index * 10007 + t * 503 + item_index * 37 + field_index)
    return rng.uniform(-noise_level, noise_level)


def _base_entity(entity_id: str, seed: int) -> dict[str, float | int | str]:
    idx = ENTITY_IDS.index(entity_id)
    offset = (idx % 4) * 0.015
    return {
        "entity_id": entity_id,
        "t": 0,
        "scenario": "objective_prediction_challenge",
        "seed": int(seed),
        "activity": 0.42 + offset,
        "volatility": 0.18 + offset,
        "uncertainty": 0.26 + offset,
        "exploration": 0.56 - offset,
        "relation_lock": 0.28 + offset,
        "coupling": 0.25 + offset,
        "reversibility": 0.68 - offset,
        "entropy": 0.60 - offset,
        "readiness": 0.55,
    }


def _apply_entity_pattern(row: dict[str, float | int | str], pattern: str, t: int, noise_level: float, seed: int) -> dict[str, float | int | str]:
    idx = ENTITY_IDS.index(str(row["entity_id"]))
    pattern_index = OBJECTIVE_PATTERNS.index(pattern)
    out = dict(row)
    rate = 0.022 * float(t)
    if pattern == "overconvergence":
        changes = {
            "exploration": -rate,
            "entropy": -rate * 0.90,
            "reversibility": -rate * 0.75,
            "relation_lock": rate * 0.95,
            "activity": -rate * 0.15,
            "volatility": -rate * 0.10,
        }
    elif pattern == "fixation":
        changes = {
            "relation_lock": rate * 1.45,
            "reversibility": -rate * 0.85,
            "activity": -rate * 0.45,
            "exploration": -rate * 0.35,
            "volatility": -rate * 0.20,
            "entropy": -rate * 0.25,
        }
    elif pattern == "divergence":
        changes = {
            "volatility": rate * 1.40,
            "uncertainty": rate * 1.15,
            "activity": rate * 0.80,
            "entropy": rate * 0.65,
            "exploration": rate * 0.35,
            "relation_lock": -rate * 0.20,
        }
    else:
        changes = {}
    numeric_fields = ["activity", "volatility", "uncertainty", "exploration", "relation_lock", "coupling", "reversibility", "entropy", "readiness"]
    for field_index, field in enumerate(numeric_fields):
        base = float(out[field]) + float(changes.get(field, 0.0))
        out[field] = _clip01(base + _noise(seed, pattern_index, t, idx, field_index, noise_level))
    out["t"] = int(t)
    return out


def _base_relation(source: str, target: str, pair_index: int, seed: int) -> dict[str, float | int | str]:
    offset = (pair_index % 4) * 0.02
    return {
        "source": source,
        "target": target,
        "relation_strength": 0.48 + offset,
        "relation_rigidity": 0.26 + offset,
        "flow": 0.24 + offset,
        "t": 0,
        "scenario": "objective_prediction_challenge",
        "seed": int(seed),
    }


def _apply_relation_pattern(row: dict[str, float | int | str], pattern: str, pair_index: int, t: int, noise_level: float, seed: int) -> dict[str, float | int | str]:
    pattern_index = OBJECTIVE_PATTERNS.index(pattern)
    out = dict(row)
    rate = 0.020 * float(t)
    if pattern == "overconvergence":
        changes = {"relation_rigidity": rate * 1.00, "flow": -rate * 0.90, "relation_strength": rate * 0.10}
    elif pattern == "fixation":
        changes = {"relation_rigidity": rate * 1.55, "flow": -rate * 1.35, "relation_strength": rate * 0.25}
    elif pattern == "divergence":
        changes = {"flow": rate * 1.50, "relation_strength": -rate * 0.25, "relation_rigidity": -rate * 0.30}
    else:
        changes = {}
    numeric_fields = ["relation_strength", "relation_rigidity", "flow"]
    for field_index, field in enumerate(numeric_fields):
        base = float(out[field]) + float(changes.get(field, 0.0))
        out[field] = _clip01(base + _noise(seed, pattern_index, t, pair_index + 100, field_index, noise_level))
    out["t"] = int(t)
    return out


def build_objective_trace(pattern: str, t: int, seed: int, noise_level: float) -> Dict[str, pd.DataFrame]:
    entity_rows = [_apply_entity_pattern(_base_entity(eid, seed), pattern, t, noise_level, seed) for eid in ENTITY_IDS]
    relation_rows = [
        _apply_relation_pattern(_base_relation(source, target, idx, seed), pattern, idx, t, noise_level, seed)
        for idx, (source, target) in enumerate(RELATION_PAIRS)
    ]
    return {"entity_trace": pd.DataFrame(entity_rows), "relation_trace": pd.DataFrame(relation_rows)}


def build_objective_trace_series(pattern: str, seed: int, noise_level: float, total_steps: int) -> list[Dict[str, pd.DataFrame]]:
    return [build_objective_trace(pattern, t, seed, noise_level) for t in range(total_steps + 1)]


def _mean_delta(current: pd.DataFrame, future: pd.DataFrame, key_cols: list[str], metric: str, horizon: int) -> float:
    if current.empty or future.empty or metric not in current.columns or metric not in future.columns:
        return 0.0
    cur = current.set_index(key_cols)
    fut = future.set_index(key_cols)
    common = cur.index.intersection(fut.index)
    if len(common) == 0:
        return 0.0
    delta = pd.to_numeric(fut.loc[common, metric], errors="coerce") - pd.to_numeric(cur.loc[common, metric], errors="coerce")
    return float(delta.fillna(0.0).mean() / max(1, int(horizon)))


def _pos(value: float) -> float:
    return max(0.0, float(value))


def score_public_dynamics_independent(current: Dict[str, pd.DataFrame], future: Dict[str, pd.DataFrame], horizon: int) -> tuple[str, float, dict[str, float]]:
    e = lambda metric: _mean_delta(current["entity_trace"], future["entity_trace"], ["entity_id"], metric, horizon)
    r = lambda metric: _mean_delta(current["relation_trace"], future["relation_trace"], ["source", "target"], metric, horizon)
    over = sum([
        _pos(-e("exploration")),
        _pos(-e("entropy")),
        _pos(-e("reversibility")),
        _pos(e("relation_lock")),
        _pos(r("relation_rigidity")),
        _pos(-r("flow")),
    ]) / 6.0
    fixation = sum([
        _pos(e("relation_lock")),
        _pos(r("relation_rigidity")),
        _pos(-r("flow")),
        _pos(-e("reversibility")),
        _pos(-e("activity")),
        _pos(-e("exploration")),
    ]) / 6.0
    divergence = sum([
        _pos(e("volatility")),
        _pos(e("uncertainty")),
        _pos(e("activity")),
        _pos(e("entropy")),
        _pos(e("exploration")),
        _pos(r("flow")),
    ]) / 6.0
    scores = {
        "overconvergence": min(1.0, over),
        "fixation": min(1.0, fixation),
        "divergence": min(1.0, divergence),
    }
    best = max(scores, key=scores.get)
    if scores[best] < 0.004:
        return "neutral", 0.0, {**scores, "neutral": 0.0}
    return best, float(scores[best]), {**scores, "neutral": 0.0}


def _prediction_for(history: list[Dict[str, pd.DataFrame]], horizon: int, seed: int, pattern: str) -> tuple[str, float]:
    current = history[-1]
    projected = project_public_trace_from_history(history, horizon)
    ot_native, ot_action_view, residual = build_public_ot_tables(current)
    gt, kt = build_placeholder_gk(current)
    out = DEPTPredictionModule().build(
        world_trace_before=current,
        baseline_trace_after=projected,
        gt=gt,
        kt=kt,
        ot_native=ot_native,
        ot_action_view=ot_action_view,
        residual_noise_log=residual,
        loop_step=int(current["entity_trace"]["t"].iloc[0]),
        seed=int(seed),
        scenario=f"objective_{pattern}",
    )
    dyn = out["dept_prediction_dynamics_projection"].iloc[0]
    return str(dyn["predicted_dynamics_direction"]), float(dyn["predicted_dynamics_strength"])


def _neutral_baseline() -> tuple[str, float]:
    return "neutral", 0.0


def _previous_step_baseline(history: list[Dict[str, pd.DataFrame]]) -> tuple[str, float]:
    if len(history) < 2:
        return _neutral_baseline()
    return score_public_dynamics_independent(history[-2], history[-1], 1)[:2]


def run_objective_challenge_case(
    pattern: str,
    seed: int,
    noise_level: float,
    cfg: PredictionObjectiveChallengeConfig,
) -> pd.DataFrame:
    total_steps = cfg.history_steps + cfg.source_steps + max(cfg.horizons) + 1
    traces = build_objective_trace_series(pattern, seed, noise_level, total_steps)
    rows: list[dict[str, object]] = []
    for source_index in range(cfg.history_steps, cfg.history_steps + cfg.source_steps):
        history = traces[: source_index + 1]
        current = history[-1]
        source_t = int(current["entity_trace"]["t"].iloc[0])
        for horizon in cfg.horizons:
            future = traces[source_index + int(horizon)]
            actual_direction, actual_strength, actual_scores = score_public_dynamics_independent(current, future, int(horizon))
            method_values = {
                PREDICTION_METHOD: _prediction_for(history, int(horizon), seed, pattern),
                NEUTRAL_BASELINE_METHOD: _neutral_baseline(),
                PREVIOUS_STEP_BASELINE_METHOD: _previous_step_baseline(history),
            }
            for method, (predicted_direction, predicted_strength) in method_values.items():
                rows.append({
                    "seed": int(seed),
                    "pattern": pattern,
                    "noise_level": float(noise_level),
                    "source_world_t": source_t,
                    "horizon": int(horizon),
                    "method": method,
                    "predicted_direction": str(predicted_direction),
                    "predicted_strength": float(predicted_strength),
                    "actual_direction": str(actual_direction),
                    "actual_strength": float(actual_strength),
                    "actual_overconvergence_score": float(actual_scores.get("overconvergence", 0.0)),
                    "actual_fixation_score": float(actual_scores.get("fixation", 0.0)),
                    "actual_divergence_score": float(actual_scores.get("divergence", 0.0)),
                    "direction_match": str(predicted_direction) == str(actual_direction),
                    "strength_abs_error": abs(float(predicted_strength) - float(actual_strength)),
                    "input_boundary_status": "public_trace_only",
                    "future_usage": "heldout_answer_key_only",
                    "forbidden_v2_trace_keys_passed_to_prediction": False,
                })
    return pd.DataFrame(rows)


def _method_summary(rows: pd.DataFrame, cfg: PredictionObjectiveChallengeConfig) -> pd.DataFrame:
    if rows.empty:
        return pd.DataFrame()
    return rows.groupby(["method", "pattern", "noise_level", "horizon"], as_index=False).agg(
        direction_match_rate=("direction_match", "mean"),
        mean_strength_abs_error=("strength_abs_error", "mean"),
        max_strength_abs_error=("strength_abs_error", "max"),
        p95_strength_abs_error=("strength_abs_error", _p95),
        mean_predicted_strength=("predicted_strength", "mean"),
        mean_actual_strength=("actual_strength", "mean"),
        rows=("direction_match", "size"),
        seeds=("seed", "nunique"),
    )


def _balanced_accuracy_summary(rows: pd.DataFrame, cfg: PredictionObjectiveChallengeConfig) -> pd.DataFrame:
    if rows.empty:
        return pd.DataFrame()
    per_pattern = rows.groupby(["method", "pattern", "noise_level", "horizon"], as_index=False).agg(
        pattern_direction_match_rate=("direction_match", "mean"),
        pattern_mean_strength_abs_error=("strength_abs_error", "mean"),
        pattern_max_strength_abs_error=("strength_abs_error", "max"),
        pattern_p95_strength_abs_error=("strength_abs_error", _p95),
    )
    return per_pattern.groupby(["method", "noise_level", "horizon"], as_index=False).agg(
        balanced_direction_match_rate=("pattern_direction_match_rate", "mean"),
        min_pattern_direction_match_rate=("pattern_direction_match_rate", "min"),
        max_pattern_direction_match_rate=("pattern_direction_match_rate", "max"),
        mean_pattern_strength_abs_error=("pattern_mean_strength_abs_error", "mean"),
        max_pattern_strength_abs_error=("pattern_max_strength_abs_error", "max"),
        p95_pattern_strength_abs_error=("pattern_p95_strength_abs_error", "max"),
        patterns=("pattern", "nunique"),
    )


def _baseline_comparison(method_summary: pd.DataFrame) -> pd.DataFrame:
    if method_summary.empty:
        return pd.DataFrame()
    pred = method_summary[method_summary["method"] == PREDICTION_METHOD]
    base = method_summary[method_summary["method"].isin([NEUTRAL_BASELINE_METHOD, PREVIOUS_STEP_BASELINE_METHOD])]
    if pred.empty or base.empty:
        return pd.DataFrame()
    pred = pred[["pattern", "noise_level", "horizon", "direction_match_rate", "mean_strength_abs_error", "max_strength_abs_error", "p95_strength_abs_error"]].rename(columns={
        "direction_match_rate": "prediction_direction_match_rate",
        "mean_strength_abs_error": "prediction_mean_strength_abs_error",
        "max_strength_abs_error": "prediction_max_strength_abs_error",
        "p95_strength_abs_error": "prediction_p95_strength_abs_error",
    })
    base = base[["method", "pattern", "noise_level", "horizon", "direction_match_rate", "mean_strength_abs_error", "max_strength_abs_error", "p95_strength_abs_error"]].rename(columns={
        "method": "baseline_method",
        "direction_match_rate": "baseline_direction_match_rate",
        "mean_strength_abs_error": "baseline_mean_strength_abs_error",
        "max_strength_abs_error": "baseline_max_strength_abs_error",
        "p95_strength_abs_error": "baseline_p95_strength_abs_error",
    })
    out = base.merge(pred, on=["pattern", "noise_level", "horizon"], how="left")
    out["direction_match_lift"] = out["prediction_direction_match_rate"] - out["baseline_direction_match_rate"]
    out["mean_strength_error_delta_vs_baseline"] = out["prediction_mean_strength_abs_error"] - out["baseline_mean_strength_abs_error"]
    out["max_strength_error_delta_vs_baseline"] = out["prediction_max_strength_abs_error"] - out["baseline_max_strength_abs_error"]
    out["p95_strength_error_delta_vs_baseline"] = out["prediction_p95_strength_abs_error"] - out["baseline_p95_strength_abs_error"]
    return out


def _seed_stability(rows: pd.DataFrame) -> pd.DataFrame:
    if rows.empty:
        return pd.DataFrame()
    per_seed = rows.groupby(["method", "pattern", "noise_level", "horizon", "seed"], as_index=False).agg(
        seed_direction_match_rate=("direction_match", "mean"),
        seed_mean_strength_abs_error=("strength_abs_error", "mean"),
        seed_max_strength_abs_error=("strength_abs_error", "max"),
    )
    return per_seed.groupby(["method", "pattern", "noise_level", "horizon"], as_index=False).agg(
        mean_seed_direction_match_rate=("seed_direction_match_rate", "mean"),
        min_seed_direction_match_rate=("seed_direction_match_rate", "min"),
        max_seed_direction_match_rate=("seed_direction_match_rate", "max"),
        std_seed_direction_match_rate=("seed_direction_match_rate", "std"),
        mean_seed_strength_abs_error=("seed_mean_strength_abs_error", "mean"),
        max_seed_strength_abs_error=("seed_mean_strength_abs_error", "max"),
        worst_seed_max_strength_abs_error=("seed_max_strength_abs_error", "max"),
        seeds=("seed_direction_match_rate", "size"),
    ).fillna(0.0)


def _direction_distribution(rows: pd.DataFrame) -> pd.DataFrame:
    if rows.empty:
        return pd.DataFrame()
    actual = rows[["pattern", "noise_level", "horizon", "actual_direction"]].drop_duplicates()
    return actual.groupby(["pattern", "noise_level", "horizon", "actual_direction"], as_index=False).size().rename(columns={"size": "count"})


def _worst_cases(method_summary: pd.DataFrame) -> pd.DataFrame:
    if method_summary.empty:
        return pd.DataFrame()
    return method_summary.sort_values(["direction_match_rate", "max_strength_abs_error"], ascending=[True, False]).head(24).reset_index(drop=True)


def _boundary(rows: pd.DataFrame, cfg: PredictionObjectiveChallengeConfig) -> pd.DataFrame:
    forbidden = bool(rows["forbidden_v2_trace_keys_passed_to_prediction"].any()) if not rows.empty else True
    directions = set(rows["actual_direction"].dropna().unique()) if not rows.empty else set()
    return pd.DataFrame([{
        "validation_name": "Prediction Objective Challenge Validation",
        "prediction_input_contract": "public_trace_history_projection_only",
        "future_usage": "heldout_answer_key_only",
        "forbidden_prediction_input_keys": ",".join(sorted(FORBIDDEN_PREDICTION_INPUT_KEYS)),
        "forbidden_v2_trace_keys_passed_to_prediction": forbidden,
        "boundary_violation_detected": forbidden or rows.empty,
        "actual_directions_seen": ",".join(sorted(directions)),
        "actual_direction_count": len(directions),
        "n_seeds": len(cfg.seeds),
        "n_patterns": len(cfg.patterns),
        "n_noise_levels": len(cfg.noise_levels),
        "n_horizons": len(cfg.horizons),
    }])


def run_prediction_objective_challenge_validation(
    cfg: PredictionObjectiveChallengeConfig | None = None,
) -> dict[str, pd.DataFrame]:
    cfg = cfg or PredictionObjectiveChallengeConfig()
    parts: list[pd.DataFrame] = []
    for seed in cfg.seeds:
        for pattern in cfg.patterns:
            for noise_level in cfg.noise_levels:
                parts.append(run_objective_challenge_case(pattern, int(seed), float(noise_level), cfg))
    rows = pd.concat([p for p in parts if p is not None and not p.empty], ignore_index=True) if parts else pd.DataFrame()
    method_summary = _method_summary(rows, cfg)
    balanced = _balanced_accuracy_summary(rows, cfg)
    comparison = _baseline_comparison(method_summary)
    stability = _seed_stability(rows)
    distribution = _direction_distribution(rows)
    worst = _worst_cases(method_summary)
    boundary = _boundary(rows, cfg)
    return {
        "prediction_objective_challenge_rows": rows,
        "prediction_objective_challenge_method_summary": method_summary,
        "prediction_objective_challenge_balanced_accuracy": balanced,
        "prediction_objective_challenge_baseline_comparison": comparison,
        "prediction_objective_challenge_seed_stability": stability,
        "prediction_objective_challenge_direction_distribution": distribution,
        "prediction_objective_challenge_worst_cases": worst,
        "prediction_objective_challenge_boundary": boundary,
    }
