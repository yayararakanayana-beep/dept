"""Task 2-7b: insurance terrain gate closed validation RC1.

Validation goal:
    v2-style dangerous state
      -> lower observation terrain extraction
      -> upper pressure projection
      -> pressure-derived six-action candidates
      -> insurance terrain gate
      -> validation-local action result
      -> improvement / side-effect audit
      -> activation-threshold sweep

Anti-arbitrariness rules:
    - dangerous states are generated by deterministic scenario templates
    - lower observation reads the generated state features, not hand-picked actions
    - upper pressure is projected from observation fields with deterministic rules
    - action candidates are produced by Task 2-6 and gated by Task 2-7
    - the validator compares surfaced candidates against no_op baseline
    - no ActionFrame, ActionModule, world runtime, or canonical write is performed
"""
